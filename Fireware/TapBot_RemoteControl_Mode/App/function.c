/***************************************************************************************************
 * @name   LED_WorkCtrlFun(void)
 * @brief  锟斤拷锟斤拷状态锟狡凤拷转锟斤拷锟斤拷
 * @param  锟斤拷
 * @note   LED1 锟斤拷 BOOT LED 同锟斤拷锟斤拷转锟斤拷锟斤拷锟节观诧拷锟斤拷循锟斤拷锟酵讹拷时锟斤拷锟斤拷锟角凤拷锟斤拷锟斤拷锟斤拷锟叫★拷
 ***************************************************************************************************/
void LED_WorkCtrlFun(void)
{
    static u8 led_toggle = 0U;

    if((led_toggle & 0x01U) == 0U)
    {
        LED1(ON);
        SW_BOOT_LED(ON);
    }
    else
    {
        LED1(OFF);
        SW_BOOT_LED(OFF);
    }

    led_toggle++;
}

